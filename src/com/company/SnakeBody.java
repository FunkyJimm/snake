package com.company;

public class SnakeBody {
    private int bodyX;
    private int bodyY;

    public SnakeBody(int bodyX, int bodyY) {
        this.bodyX = bodyX;
        this.bodyY = bodyY;
    }

    public int getBodyX() {
        return bodyX;
    }

    public void setBodyX(int bodyX) {
        this.bodyX = bodyX;
    }

    public int getBodyY() {
        return bodyY;
    }

    public void setBodyY(int bodyY) {
        this.bodyY = bodyY;
    }
}
