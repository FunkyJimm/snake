package com.company;

public class Fruit {
    private int fruitX;
    private int fruitY;

    public Fruit(int fruitX, int fruitY) {
        this.fruitX = fruitX;
        this.fruitY = fruitY;
    }

    public int getFruitX() {
        return fruitX;
    }

    public int getFruitY() {
        return fruitY;
    }
}
